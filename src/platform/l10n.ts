export function t(key: string, ...args: (string | number)[]): string {
  let text: string;
  try {
    text = vscode.l10n.t(key);
    if (text === key) {
      text = FALLBACK[key] ?? key;
    }
  } catch {
    text = FALLBACK[key] ?? key;
  }
  return formatArgs(text, args);
}

/** Resolve string for webview HTML (escape separately). */
export function tRaw(key: string, ...args: (string | number)[]): string {
  return t(key, ...args);
}

export function loadLocaleBundle(): string {
  return vscode.env.language;
}

export function formatArgs(text: string, args: (string | number)[]): string {
  return args.reduce<string>(
    (acc, arg, i) => acc.replace(`{${i}}`, String(arg)),
    text
  );
}
